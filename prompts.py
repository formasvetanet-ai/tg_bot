def get_system_prompt(section_key):
    prompts = {
        "supplier": CONSTITUTION + "\n\n" + SKILL_SUPPLIER,
        "delivery": CONSTITUTION + "\n\n" + SKILL_DELIVERY,
        "inspection": CONSTITUTION + "\n\n" + SKILL_INSPECTION,
        "branding": CONSTITUTION + "\n\n" + SKILL_BRANDING,
        "consultation": CONSTITUTION + "\n\n" + SKILL_CONSULTATION,
    }
    return prompts.get(section_key, CONSTITUTION)


def format_answers(section_key, answers, questions_map):
    questions = questions_map.get(section_key, [])
    result = []
    for i, answer in enumerate(answers):
        if i < len(questions):
            q = questions[i].split(":")[0].strip()
            q = q.split("?")[0].strip()
            q = q.split("\n")[0].strip()
            result.append(f"{q}: {answer}")
    return "\n".join(result)
